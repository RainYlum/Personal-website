UNION ALL
SELECT '考勤表', COUNT(*) FROM attendance
UNION ALL
SELECT '请假表', COUNT(*) FROM leave_requests
UNION ALL
SELECT '薪酬表', COUNT(*) FROM payroll
UNION ALL
SELECT '招聘表', COUNT(*) FROM recruitment;

